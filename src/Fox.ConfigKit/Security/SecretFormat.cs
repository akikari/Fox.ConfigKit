//==================================================================================================
// Enumeration of supported secret storage formats.
// Used for validating that secrets follow secure storage patterns.
//==================================================================================================

namespace Fox.ConfigKit.Security;

//==============================================================================================
/// <summary>
/// Defines the expected format for secret storage.
/// </summary>
//==============================================================================================
public enum SecretFormat
{
    //==============================================================================================
    /// <summary>
    /// Azure Key Vault reference format (e.g., @Microsoft.KeyVault(...)).
    /// </summary>
    //==============================================================================================
    AzureKeyVault,

    //==============================================================================================
    /// <summary>
    /// AWS Secrets Manager reference format.
    /// </summary>
    //==============================================================================================
    AwsSecretsManager,

    //==============================================================================================
    /// <summary>
    /// Environment variable reference format (e.g., ${ENV_VAR}).
    /// </summary>
    //==============================================================================================
    EnvironmentVariable,

    //==============================================================================================
    /// <summary>
    /// User secrets reference (used in development).
    /// </summary>
    //==============================================================================================
    UserSecrets
}
