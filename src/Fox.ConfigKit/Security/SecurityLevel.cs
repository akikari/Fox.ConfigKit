//==================================================================================================
// Enumeration of security warning levels.
// Used to indicate the severity of security validation issues.
//==================================================================================================

namespace Fox.ConfigKit.Security;

//==============================================================================================
/// <summary>
/// Defines the severity level of a security warning.
/// </summary>
//==============================================================================================
public enum SecurityLevel
{
    //==============================================================================================
    /// <summary>
    /// Informational message.
    /// </summary>
    //==============================================================================================
    Info,

    //==============================================================================================
    /// <summary>
    /// Warning about potential security issue.
    /// </summary>
    //==============================================================================================
    Warning,

    //==============================================================================================
    /// <summary>
    /// Critical security issue.
    /// </summary>
    //==============================================================================================
    Critical
}
